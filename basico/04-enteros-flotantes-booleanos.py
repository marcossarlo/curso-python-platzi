# Comprender los diferentes tipos de datos en Python es crucial para la programación eficiente. En Python, cada variable pertenece a una clase o “class”, y su tipo puede ser identificado usando la función type().


x = 10
y = 45.789
z = 1E6
w = 1.3e-4

print(x, type(x))
print(y, type(y))
print(z, type(z))
print(w, type(w))

print(x + x)
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)

isTrue = True
is_false = False
print(isTrue, type(isTrue))
print(is_false, type(is_false))


